    static public int[] maxNumber(int[] nums1, int[] nums2, int k) {
        // 321. Create Maximum Number
        // https://leetcode.com/problems/create-maximum-number/

        long tt = System.nanoTime();
        int n1 = nums1.length;
        int n2 = nums2.length;

        char[] a = new char[1000];
        char[] b = new char[1000];

        int i, j, t;

        for (i = nums1.length - 1; i >= 0; i--){
            a[nums1.length - 1 - i] = (char) (nums1[i]);
        }

        for (i = nums2.length - 1; i >= 0; i--){
            b[nums2.length - 1 - i] = (char) (nums2[i]);
        }

        String[][][] F = new String[k + 1][n1 + 1][n2 + 1];
        for (t = 0; t <= k; t++)
            for (i = 0; i <= n1; i++)
                for (j = 0; j <= n2; j++)
                    F[t][i][j] = "";

        for (t = 1; t <= k; t++){
            for (i = t; i <= n1; i++)
                if (String.valueOf(a[i - 1] + F[t - 1][i - 1][0]).compareTo(F[t][i - 1][0]) > 0){
                    F[t][i][0] = String.valueOf(a[i - 1] + F[t - 1][i - 1][0]);
                } else {
                    F[t][i][0] = F[t][i - 1][0];
                }
        }

        int[] x = {1};
        System.out.print((System.nanoTime() - tt) / 1000000000);
        return x;

    }

